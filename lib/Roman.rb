class RomanNumerals 

  def convert(num) 
    if num == 0
      ""
    elsif num == 1
      "I"
    elsif num == 2
      "II"
    elsif num == 3
      "III"
    elsif num == 4
      "IIII"
    elsif num == 5
      "V"
    elsif num == 6
      "VI"
    elsif num == 7
      "VII"
    elsif num == 8
      "VIII"
    elsif num == 9
      "VIIII"
    elsif num == 10
      "X"
    end  
  end
end 
